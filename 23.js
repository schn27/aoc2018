function manhDist(a, b, n) {
	if (!Array.isArray(a)) {
		return Math.abs(a - b);
	} else {
		let res = 0;

		for (let i = 0; i < (n || Math.min(a.length, b.length)); ++i) {
			res += Math.abs(a[i] - b[i]);
		}

		return res;
	}
}

